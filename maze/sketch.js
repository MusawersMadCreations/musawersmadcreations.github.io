function setup() {
  createCanvas(windowWidth, windowHeight);
  cellSize = 50;
  frameRate();

}


function draw() {
  background(255);
  drawBoard();
  print(frameCount);
}


function drawBoard() {
  for (let x = 0; x < rows; x++) {
    for (let y = 0; y < cols; y++) {
      if (level1[x][y] === 0) {
        fill(0);
        rect(x * cellSize, y * cellSize, cellSize, cellSize);
      }

      else {
        fill(120);
        rect(x * cellSize, y * cellSize, cellSize, cellSize);
      }
    }
  }
}
