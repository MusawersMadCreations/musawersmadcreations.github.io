let cellSize;
let rows = 25;
let cols = 25;


function setup() {
  createCanvas(windowWidth, windowHeight);
  cellSize = 50;
  frameRate(10);

}


function draw() {
  background(255);
  drawBoard();
  print(frameCount);
}


function drawBoard() {
  for (let x = 0; x < cols; x++) {
    for (let y = 0; y < rows; y++) {
      if (level1[x][y] === 0) {
        fill(0);
        rect(x * cellSize, y * cellSize, cellSize, cellSize);
      }
      else {
        fill(255);
        rect(x * cellSize, y * cellSize, cellSize, cellSize);
      }

    }
  }
}
function shrinkmaze(){
  if (frameCount >= 50) {
    level1[1] = increaseWall;
  }
}
